# 인덱싱
#문자열 연결 연산잔
print("abc" + "abc")


# 문자열 반복 연산자
print("abc" * 3)

# 문자 선택 연산자
print("abcde"[0])
print("abcde"[4])



#0~2 인덱스 출력
print("abcde"[0:3])

#2인덱스 부터 출력
print("abcde"[2:])

#다 출력
print("abcde"[:])



# 문자열[인덱스:인덱스:스텝]
print("안녕하세요"[1:4:2])



# 슬라이싱
# 오류는 발생하지 않으나 이상한 코드다.
print("안녕하세요"[1:00])


#문자열의 길이
print(len("안녕하세요오오오오오오"))




